    }

    public function aboutme()
    {
       $post=AboutMe::where('is_active','=',1)->orderBy('updated_at','desc')->get()->first();
       $post->body=str_replace("\r\n","<br>",$post->body);
       return view('post.post-about',compact('post'));
    }
}
